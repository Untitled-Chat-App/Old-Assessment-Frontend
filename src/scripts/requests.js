const fetch = require("node-fetch");
const rsa = require("node-rsa");
const Store = require("electron-store");
const { URLSearchParams } = require("url");

const store = new Store();
require("dotenv").config();
const API_URL = "https://chatapi.fusionsid.xyz";

async function getAccessToken(username = null, password = null, scopes = null) {
    let token;

    if (scopes === null) {
        scopes = ""
    }
    // Format login details into query params
    let login_details = new URLSearchParams({
        username: username,
        password: password,
        scope: scopes
    }).toString();

    await fetch(`${API_URL}/token`, {
        method: "POST",
        headers: {
            Accept: "application/json",
            "Content-Type": "application/x-www-form-urlencoded",
        },
        body: login_details,
    })
        .then((response) => response.json())
        .then((data) => {
            token = data["access_token"];
        });

    return token;
}

async function signupUser(data) {
    let keys = new rsa().generateKeyPair();
    let publicKey = keys.exportKey("public");
    let privateKey = keys.exportKey("private");

    store.set("private_key", privateKey); // You delete my app too bad

    // Create new user
    await fetch(`${API_URL}/api/users/signup`, {
        method: "POST",
        headers: {
            "Content-Type": "application/json",
        },
        body: JSON.stringify({
            username: data.username,
            email: data.email,
            password: data.password,
            public_key: JSON.stringify(publicKey),
        }),
    })
        .then((response) => response.json())
        .then((json_data) => {
            data = json_data;
        });

    return data;
}

async function getUserWithToken(token) {
    let data;

    await fetch(`${API_URL}/api/user/me`, {
        method: "GET",
        headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${token}`,
        },
    })
        .then((response) => response.json())
        .then((json_data) => {
            data = json_data;
        });

    return data;
}

async function updateUserDetails(attribute, new_value) {
    let access_token = await getAccessToken(store.get("password"), store.get("username"), "mofify_self");

    await fetch(`${API_URL}/api/user/me`, {
        method: "PATCH",
        headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${access_token}`,
        },
        body: JSON.stringify({
            attribute: attribute,
            new_value: new_value,
        }),
    })
        .then((response) => response.json())
        .then((json_data) => {
            data = json_data;
        });

    return data;
}

async function createNewRoom(room_name, room_description=null) {
    let access_token = await getAccessToken(store.get("username"), store.get("password"), "create_rooms");

    await fetch(`${API_URL}/api/chatroom/new`, {
        method: "POST",
        headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${access_token}`,
        },
        body: JSON.stringify({
            room_name: room_name,
            room_description: room_description,
        }),
    })
        .then((response) => response.json())
        .then((json_data) => {
            data = json_data;
        });

    return data;
}

async function getRoomById(room_id) {
    let access_token = await getAccessToken(
        store.get("username"),
        store.get("password")
    );

    await fetch(`${API_URL}/api/chatroom/${room_id}`, {
        method: "GET",
        headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${access_token}`,
        },
    })
        .then((response) => response.json())
        .then((json_data) => {
            data = json_data;
        });

    return data;
}

